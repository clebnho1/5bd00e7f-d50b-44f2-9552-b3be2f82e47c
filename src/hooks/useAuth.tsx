import React, { useState, useEffect, createContext, useContext, ReactNode } from 'react';
import { User, Session } from '@supabase/supabase-js';
import { supabase } from '@/integrations/supabase/client';
import { useToast } from '@/hooks/use-toast';
import { Database } from '@/integrations/supabase/types';

type UserRole = Database['public']['Enums']['user_role'];

interface AuthContextType {
  user: User | null;
  session: Session | null;
  signIn: (email: string, password: string) => Promise<void>;
  signUp: (email: string, password: string, name: string, plano: string) => Promise<void>;
  signOut: () => Promise<void>;
  resetPassword: (email: string) => Promise<void>;
  loading: boolean;
  isAdmin: () => boolean;
  userRole: UserRole | null;
  refreshUserRole: () => Promise<void>;
}

const AuthContext = createContext<AuthContextType | undefined>(undefined);

export function AuthProvider({ children }: { children: ReactNode }) {
  const [user, setUser] = useState<User | null>(null);
  const [session, setSession] = useState<Session | null>(null);
  const [loading, setLoading] = useState(true);
  const [userRole, setUserRole] = useState<UserRole | null>(null);
  const { toast } = useToast();

  console.log('üîß [AUTH_PROVIDER] Estado atual:', { 
    hasUser: !!user, 
    hasSession: !!session, 
    loading, 
    userRole 
  });

  const fetchUserRole = async (userId: string) => {
    try {
      console.log('üîç [ROLE] Buscando role para usu√°rio:', userId);
      
      const { data, error } = await supabase
        .from('users')
        .select('role')
        .eq('id', userId)
        .single();

      if (error) {
        console.error('‚ùå [ROLE_ERROR]', error);
        setUserRole('user');
        return;
      }

      console.log('‚úÖ [ROLE_SUCCESS] Role encontrado:', data.role);
      setUserRole(data.role);
    } catch (error) {
      console.error('üí• [ROLE_CRASH]', error);
      setUserRole('user');
    }
  };

  useEffect(() => {
    console.log('üîÑ [INIT] Iniciando autentica√ß√£o');
    
    let isMounted = true;

    const initializeAuth = async () => {
      try {
        // PRIMEIRO: Obter sess√£o atual
        const { data: { session: currentSession } } = await supabase.auth.getSession();
        
        console.log('üì® [SESSION] Sess√£o obtida:', {
          hasSession: !!currentSession,
          hasUser: !!currentSession?.user
        });

        if (isMounted) {
          setSession(currentSession);
          setUser(currentSession?.user ?? null);
          
          if (currentSession?.user) {
            await fetchUserRole(currentSession.user.id);
          }
          
          setLoading(false);
          console.log('‚úÖ [INIT] Inicializa√ß√£o conclu√≠da');
        }

        // SEGUNDO: Configurar listener para mudan√ßas futuras
        const { data: { subscription } } = supabase.auth.onAuthStateChange(
          async (event, newSession) => {
            if (!isMounted) return;
            
            console.log('üîÑ [AUTH_CHANGE] Evento:', {
              event,
              hasSession: !!newSession,
              hasUser: !!newSession?.user
            });
            
            setSession(newSession);
            setUser(newSession?.user ?? null);
            
            if (newSession?.user) {
              await fetchUserRole(newSession.user.id);
            } else {
              setUserRole(null);
            }
          }
        );

        return subscription;

      } catch (error) {
        console.error('üí• [INIT_ERROR]', error);
        if (isMounted) {
          setLoading(false);
        }
      }
    };

    let subscriptionPromise = initializeAuth();

    return () => {
      isMounted = false;
      subscriptionPromise.then(subscription => {
        if (subscription) {
          subscription.unsubscribe();
        }
      });
      console.log('üßπ [CLEANUP] Limpando AuthProvider');
    };
  }, []); // Depend√™ncias vazias s√£o corretas aqui

  const refreshUserRole = async () => {
    if (user) {
      console.log('üîÑ [REFRESH_ROLE] Atualizando role do usu√°rio');
      await fetchUserRole(user.id);
    }
  };

  const isAdmin = () => {
    return userRole === 'admin' || user?.email === 'admin@admin.com';
  };

  const signIn = async (email: string, password: string) => {
    if (!email.trim() || !password.trim()) {
      const errorMsg = "Email e senha s√£o obrigat√≥rios";
      toast({
        title: "Campos obrigat√≥rios",
        description: errorMsg,
        variant: "destructive",
      });
      throw new Error(errorMsg);
    }

    try {
      console.log('üîê [SIGNIN] Tentativa de login para:', email);
      
      const { error } = await supabase.auth.signInWithPassword({
        email: email.trim(),
        password,
      });

      if (error) {
        let errorMessage = "Erro no login";
        
        if (error.message.includes('Invalid login credentials')) {
          errorMessage = "Email ou senha incorretos";
        } else if (error.message.includes('Email not confirmed')) {
          errorMessage = "Email n√£o confirmado. Verifique sua caixa de entrada";
        } else if (error.message.includes('Too many requests')) {
          errorMessage = "Muitas tentativas. Tente novamente em alguns minutos";
        }

        toast({
          title: "Erro no login",
          description: errorMessage,
          variant: "destructive",
        });
        throw error;
      }

      console.log('‚úÖ [SIGNIN] Login bem-sucedido');
      
      toast({
        title: "Login realizado com sucesso!",
        description: "Bem-vindo de volta!",
      });
      
    } catch (error) {
      console.error('üí• Erro durante sign in:', error);
      throw error;
    }
  };

  const signUp = async (email: string, password: string, name: string, plano: string) => {
    if (!email.trim() || !password.trim() || !name.trim()) {
      const errorMsg = "Todos os campos s√£o obrigat√≥rios";
      toast({
        title: "Campos obrigat√≥rios",
        description: errorMsg,
        variant: "destructive",
      });
      throw new Error(errorMsg);
    }

    if (password.length < 6) {
      const errorMsg = "A senha deve ter pelo menos 6 caracteres";
      toast({
        title: "Senha muito fraca",
        description: errorMsg,
        variant: "destructive",
      });
      throw new Error(errorMsg);
    }

    try {
      const { data, error } = await supabase.auth.signUp({
        email: email.trim(),
        password,
        options: {
          data: {
            name: name.trim(),
            plano: plano,
          },
        },
      });

      if (error) {
        let errorMessage = "Erro no cadastro";
        
        if (error.message.includes('User already registered') || 
            error.message.includes('duplicate key value violates unique constraint')) {
          errorMessage = "Este email j√° est√° cadastrado";
          
          toast({
            title: "Email j√° cadastrado",
            description: "Este email j√° possui uma conta. Tente fazer login.",
            variant: "destructive",
          });
          
          throw new Error(errorMessage);
        } else if (error.message.includes('Password should be at least')) {
          errorMessage = "A senha deve ter pelo menos 6 caracteres";
        } else if (error.message.includes('Invalid email')) {
          errorMessage = "Email inv√°lido";
        }

        toast({
          title: "Erro no cadastro",
          description: errorMessage,
          variant: "destructive",
        });
        throw error;
      }

      if (data.user && !data.session) {
        toast({
          title: "Verifique seu email",
          description: "Um link de confirma√ß√£o foi enviado para seu email.",
        });
      } else if (data.session) {
        toast({
          title: "Cadastro realizado com sucesso!",
          description: "Bem-vindo ao sistema!",
        });
      }
    } catch (error) {
      console.error('Error during sign up:', error);
      throw error;
    }
  };

  const signOut = async () => {
    try {
      const { error } = await supabase.auth.signOut();
      
      if (error) {
        console.error('Sign out error:', error);
        toast({
          title: "Erro ao sair",
          description: error.message || "Erro desconhecido",
          variant: "destructive",
        });
        throw error;
      }

      setUserRole(null);
      
      toast({
        title: "Logout realizado",
        description: "At√© logo!",
      });
    } catch (error) {
      console.error('Error during sign out:', error);
      throw error;
    }
  };

  const resetPassword = async (email: string) => {
    if (!email.trim()) {
      const errorMsg = "Email √© obrigat√≥rio";
      toast({
        title: "Campo obrigat√≥rio",
        description: errorMsg,
        variant: "destructive",
      });
      throw new Error(errorMsg);
    }

    try {
      const { error } = await supabase.auth.resetPasswordForEmail(email.trim(), {
        redirectTo: `${window.location.origin}/reset-password`,
      });

      if (error) {
        console.error('Password reset error:', error);
        toast({
          title: "Erro ao enviar email",
          description: error.message || "Erro desconhecido",
          variant: "destructive",
        });
        throw error;
      }

      toast({
        title: "Email enviado!",
        description: "Verifique sua caixa de entrada para redefinir sua senha.",
      });
    } catch (error) {
      console.error('Error during password reset:', error);
      throw error;
    }
  };

  const contextValue = {
    user,
    session,
    signIn,
    signUp,
    signOut,
    resetPassword,
    loading,
    isAdmin,
    userRole,
    refreshUserRole,
  };

  return (
    <AuthContext.Provider value={contextValue}>
      {children}
    </AuthContext.Provider>
  );
}

export function useAuth() {
  const context = useContext(AuthContext);
  if (context === undefined) {
    throw new Error('useAuth must be used within an AuthProvider');
  }
  
  return context;
}
